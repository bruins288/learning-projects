# Дан список чисел: [45, 12, 78, 34, 89, 23, 67]
# Найди максимальное число с помощью цикла (не используя max())

numbers = [45, 12, 78, 34, 89, 23, 67]

max_num = numbers[0]
for num in numbers:
    if num > max_num:
        max_num = num
print(f"Максимальное число: {max_num}")

