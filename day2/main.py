# Это комментарий. Python его игнорирует.
# Комментарии нужны, чтобы объяснять код себе и другим.

# --- Простые типы данных ---

# 1. Числа (int - целые числа)
year = 2024
population = 146_000_000 #Нижнее подчеркивание просто для удобства чтения
print("Год:", year)
print("Население:", population)

# 2. Числа с плавающей точкой (float - десятичные дроби)
gdp_growth = 2.5 # ВВП вырос на 2.5 процента
inflation_rate = 7.8
print("Рост ВВП:", gdp_growth)
print("Инфляция:", inflation_rate)

# 3. Строки (str - текст)
country_name = "Россия"
currency = "RUB"
message = 'Данные по стране:'  # Можно и в одинарных кавычках
print(message, country_name)

# 4. Булевы значения (bool - истина или ложь)
is_europe = True  # Россия в Европе? Частично да
is_eu_member = False  # Член ЕС? Нет
print("Находится в Европе:", is_europe)
print("Член ЕС:", is_eu_member)

# --- Немного магии с переменными ---

# Можно менять значение переменной
country_name = "США"
print("Теперь страна:", country_name)

# Можно создавать новые переменные на основе старых
next_year_gdp = gdp_growth + 1.5  # Прогноз на следующий год
print("Прогноз роста на следующий год:", next_year_gdp)

# --- Ввод данных от пользователя ---
# Функция input() ждет, пока пользователь что-то введет
user_country = input("Введите название страны: ")
print("Вы ввели:", user_country)

# Важно! input() всегда возвращает строку. Даже если ввели число.
user_gdp = input("Введите ВВП страны: ")
# Превращаем строку в число с плавающей точкой
user_gdp_float = float(user_gdp)
print("ВВП, умноженный на 2:", user_gdp_float * 2)